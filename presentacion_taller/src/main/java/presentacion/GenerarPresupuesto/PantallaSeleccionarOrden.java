   private void generarListaOrdenes() {
    List<OrdenDTO> ordenes = controlOrdenes.buscarOrdenesPorCliente(clienteSeleccionado);


    JPanel contenedor = new JPanel();
    contenedor.setLayout(new BoxLayout(contenedor, BoxLayout.Y_AXIS));
    contenedor.setOpaque(false);

    if (ordenes == null || ordenes.isEmpty()) {
        JPanel panelVacio = creacion.crearPanelInformativo("Este cliente no tiene Ã³rdenes registradas.", true);
        contenedor.add(panelVacio);

    } else {
        
        for (OrdenDTO o : ordenes) {

            String texto =
                    "Folio: " + o.getIdOrden() +
                    "  |  Falla: " + o.getFallaReportada();

            JPanel panelOrden = creacion.crearPanelOrden(texto);

            panelOrden.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
            panelOrden.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    navegacion.mostrarPantallaGenerarPresupuesto(navegacion, creacion, o, clienteSeleccionado, presupuesto, servicio, controlServicios, controlRefacciones, controlPresupuesto, mensajes, clientes);
                    dispose();
                }
            });

            contenedor.add(panelOrden);
            contenedor.add(creacion.crearSeparador(8));
        }
    }
    scrollPaneOrdenes.setViewportView(contenedor);
}
  

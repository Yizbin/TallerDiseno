        if (tareas.isEmpty()) {
            mensajes.mostrarError(this, "No hay tareas para imprimir.");
            return;
        }
        String nombre = mecanicoActual != null ? mecanicoActual.getNombre() : "Mecánico";
        controlDocumentos.imprimirReporteTareasCompletadas(tareas, nombre);

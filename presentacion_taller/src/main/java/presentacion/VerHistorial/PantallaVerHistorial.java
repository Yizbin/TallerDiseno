    private List<TareaDTO> obtenerTareasDeLaTabla() {
        List<TareaDTO> lista = new ArrayList<>();

        int filas = modeloTabla.getRowCount();

        for (int i = 0; i < filas; i++) {
            TareaDTO tarea = new TareaDTO();

            tarea.setIdTarea((String) modeloTabla.getValueAt(i, 0));
            tarea.setDescripcion((String) modeloTabla.getValueAt(i, 1));
            tarea.setVehiculoModelo((String) modeloTabla.getValueAt(i, 2));
            tarea.setIdOrden((String) modeloTabla.getValueAt(i, 3));
            tarea.setEstado((String) modeloTabla.getValueAt(i, 4));

            lista.add(tarea);
        }

        return lista;
    }

    private void configurarModeloTabla() {
        String[] columnas = {"ID", "Descripción", "Vehículo", "Orden", "Estado"};

        modeloTabla = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        jTable1.setModel(modeloTabla);

        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
    }

    private void estilizarTabla() {
        creacion.aplicarEstiloTabla(jScrollPane1, jTable1);
    }

    private void cargarHistorial() {
        modeloTabla.setRowCount(0);

        if (mecanicoActual == null) {
            mensajes.mostrarError(this,
                    "No se encontró información del mecánico.");

            return;
        }

        List<TareaDTO> lista = controlTareas.obtenerTareasCompletadasPorMecanico(
                mecanicoActual.getId_empleado()
        );

        for (TareaDTO t : lista) {
            modeloTabla.addRow(new Object[]{
                t.getIdTarea(),
                t.getDescripcion(),
                t.getVehiculoModelo(),
                t.getIdOrden(),
                t.getEstado()
            });
        }
    }


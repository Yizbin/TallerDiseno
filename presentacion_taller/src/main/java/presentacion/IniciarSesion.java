    private void configurarVentana() {
        this.setLocationRelativeTo(null);
    }

    private Boolean validarCampos() {
        String usuario = correoText.getText();
        String contra = contraseniaText.getText();

        try {
            control.validarCampoVacio(usuario, "Correo");
            control.validarCampoVacio(contra, "Contrase単a");
        } catch (ValidacionException ex) {
            control.mostrarErrorCampos(ex.getMessage());
            return false;
        }
        return true;
    }

    private void iniciarSesion() {
        if (validarCampos()) {
            String usuario = correoText.getText();
            String contra = contraseniaText.getText();

            if (control.autenticarUsuario(usuario, contra)) {
                this.navegar();
            } else {
                control.mostrarErrorCampos("Usuario o contrase単a incorrectos");
            }
        }
    }

    private void navegar() {
        navegacion.mostrarMenuPrincipal();
        this.dispose();
    }


